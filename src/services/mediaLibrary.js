function normalize(text) {
  return String(text || "")
    .toLowerCase()
    .replace(/[^\p{L}\p{N}\s]/gu, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function toPublicUrl(publicBaseUrl, filename) {
  if (!publicBaseUrl || !filename) {
    return null;
  }

  return `${publicBaseUrl.replace(/\/+$/, "")}/media/thesis-master/${filename}`;
}

function materialize(entry, publicBaseUrl) {
  if (!entry) {
    return null;
  }

  return {
    ...entry,
    intent: entry.intents[0] || null,
    url: toPublicUrl(publicBaseUrl, entry.filename),
  };
}

export function listMedia(publicBaseUrl) {
  return mediaCatalog.map((entry) => materialize(entry, publicBaseUrl));
}

export function getSampleImage(publicBaseUrl) {
  return materialize(
    mediaCatalog.find((entry) => entry.key === "sample_request"),
    publicBaseUrl
  );
}

export function getMediaByIntent(intent, publicBaseUrl) {
  if (!intent) {
    return null;
  }

  return materialize(
    mediaCatalog.find((entry) => entry.intents.includes(intent)),
    publicBaseUrl
  );
}

export function resolveMediaForRequest({
  text,
  matchedIntent,
  publicBaseUrl,
}) {
  const normalized = normalize(text);
  const byIntent = getMediaByIntent(matchedIntent, publicBaseUrl);

  if (!normalized) {
    return byIntent || getSampleImage(publicBaseUrl);
  }

  let bestMatch = null;
  let bestScore = 0;

  for (const entry of mediaCatalog) {
    if (entry.key === "sample_request") {
      continue;
    }

    let score = 0;

    if (matchedIntent && entry.intents.includes(matchedIntent)) {
      score += 5;
    }

    for (const tag of entry.tags) {
      if (normalized.includes(tag)) {
        score += 2;
      }
    }

    if (score > bestScore) {
      bestScore = score;
      bestMatch = entry;
    }
  }

  return (
    materialize(bestMatch, publicBaseUrl) ||
    byIntent ||
    getSampleImage(publicBaseUrl)
  );
}
